int	ft_sqrt(int nb)
{
	unsigned int	i;
	unsigned int	y;

	y = nb;
	i = 1;
	if (y <= 0)
		return (0);
	while (i * i < y)
	{
		i++;
	}
	if (i * i == y)
	{
		return (i);
	}
	return (0);
}
