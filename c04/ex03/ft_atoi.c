int	ft_atoi(char *str)

{
	unsigned int	i;
	unsigned int	j;
	unsigned int	n;

	i = 0;
	j = 0;
	n = 0;
	while ((str[i] != '\0')
		|| (str[i] == 32)
		|| (str[i] == '+')
		|| (str[i] >= '0' && str[i] <= '9')
		|| (str[i] == '-'))
	{
		if (str[i] == '-')
			j++;
		if (str[i] >= '0' && str[i] <= '9')
			n = n * 10 + (str[i] - '0');
		i++;
	}
	if (j % 2 == 1)
		j = -1;
	else
		j = 1;
	return (n * j);
}
